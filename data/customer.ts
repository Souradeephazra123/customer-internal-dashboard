export function getCustomerById(id: string): Customer | undefined {
  return CUSTOMERS.find((c) => c.customerId === id);
}

export function getCustomerName(id: string): string {
  return getCustomerById(id)?.companyName ?? id;
}

export function getCustomerTenureMonths(contractStart: string): number {
  const [month, day, year] = contractStart.split("/").map(Number);
  const start = new Date(year, month - 1, day);
  const now = new Date();
  return (
    (now.getFullYear() - start.getFullYear()) * 12 +
    (now.getMonth() - start.getMonth())
  );
}